// 按批次分组（腾讯API限制每次最多约100只）
export function batchCodes(codes, batchSize = 100) {
  const batches = [];
  for (let i = 0; i < codes.length; i += batchSize) {
    batches.push(codes.slice(i, i + batchSize));
  }
  return batches;
}

