// 获取股票所属板块
export function getStockSector(stockCode) {
  for (const [sector, codes] of Object.entries(stockBySector)) {
    if (codes.includes(stockCode)) {
      return sector;
    }
  }
  return '其他';
}

// 按板块分组股票
export function groupStocksBySector(stocks) {
  const groups = {};
  
  stocks.forEach(stock => {
    const sector = getStockSector(stock.code);
    if (!groups[sector]) {
      groups[sector] = [];
    }
    groups[sector].push(stock);
  });
  
  return groups;
}

// 按批次分组（腾讯API限制每次最多约100只）
export function batchCodes(codes, batchSize = 100) {
  const batches = [];
  for (let i = 0; i < codes.length; i += batchSize) {
    batches.push(codes.slice(i, i + batchSize));
  }
  return batches;
}

